create or replace view DW_PROD.REPORT.VW_EMPLOYEE_DERIVED_WEEKLY_METRICS_BY_SERVICE_LINE(
	SERVICE_WEEK,
	EMPLOYEE_KEY,
	SOURCE_SYSTEM_ID,
	SYSTEM_CODE,
	EMPLOYEE_ID,
	EMPLOYEE_PID,
	EMPLOYEE_NUMBER,
	PAYROLL_ID,
	BRANCH_KEY,
	BRANCH_NAME,
	BRANCH_STATE_CODE,
	SERVICE_LINE,
	NUMBER_OF_VISITS,
	NUMBER_OF_DISTINCT_CLIENTS,
	SUM_OF_HOURS_SERVED,
	ETL_TASK_KEY,
	ETL_INSERTED_TASK_KEY,
	ETL_INSERTED_DATE,
	ETL_INSERTED_BY,
	ETL_LAST_UPDATED_DATE,
	ETL_LAST_UPDATED_BY,
	ETL_DELETED_FLAG
) as
SELECT * FROM REPORT.EMPLOYEE_DERIVED_WEEKLY_METRICS_BY_SERVICE_LINE
WHERE SERVICE_WEEK BETWEEN DATE_TRUNC('YEAR',DATEADD('YEAR',-2,CURRENT_DATE)) AND CURRENT_DATE;