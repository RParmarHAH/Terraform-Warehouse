create or replace view DW_PROD.REPORT.VW_EMPLOYEE_DERIVED_MONTHLY_METRICS_BY_SERVICE_LINE(
	SERVICE_MONTH,
	EMPLOYEE_KEY,
	SOURCE_SYSTEM_ID,
	SYSTEM_CODE,
	EMPLOYEE_ID,
	EMPLOYEE_PID,
	EMPLOYEE_NUMBER,
	PAYROLL_ID,
	FIRST_NAME,
	LAST_NAME,
	BRANCH_KEY,
	BRANCH_NAME,
	BRANCH_STATE_CODE,
	SERVICE_LINE,
	NUMBER_OF_VISITS,
	NUMBER_OF_DISTINCT_CLIENTS,
	SUM_OF_HOURS_SERVED
) as
SELECT * FROM REPORT.EMPLOYEE_DERIVED_MONTHLY_METRICS_BY_SERVICE_LINE
WHERE SERVICE_MONTH BETWEEN DATE_TRUNC('YEAR',DATEADD('YEAR',-2,CURRENT_DATE)) AND CURRENT_DATE;