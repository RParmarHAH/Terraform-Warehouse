create or replace view DW_PROD.INTEGRATION.VW_DIM_EMPLOYEE_NY(
	ID,
	NAME,
	SYSTEM_CODE,
	EMPLOYEE_LAST_NAME,
	EMPLOYEE_FIRST_NAME,
	EMPLOYEE_PID,
	EMPLOYEE_DOB,
	EMPLOYEE_ADDRESS1,
	EMPLOYEE_CITY,
	EMPLOYEE_HOME_PHONE,
	EMPLOYEE_PERSONAL_EMAIL
) as
SELECT EMPLOYEE_KEY AS ID, EMPLOYEE_FIRST_NAME || EMPLOYEE_LAST_NAME AS NAME,  SYSTEM_CODE,EMPLOYEE_LAST_NAME,EMPLOYEE_FIRST_NAME,EMPLOYEE_PID,EMPLOYEE_DOB,EMPLOYEE_ADDRESS1,EMPLOYEE_CITY,EMPLOYEE_HOME_PHONE,EMPLOYEE_PERSONAL_EMAIL FROM DW_PROD.HAH.DIM_EMPLOYEE WHERE SOURCE_SYSTEM_ID = 17 AND SYSTEM_CODE IN ('PREFERRED','EDISON');