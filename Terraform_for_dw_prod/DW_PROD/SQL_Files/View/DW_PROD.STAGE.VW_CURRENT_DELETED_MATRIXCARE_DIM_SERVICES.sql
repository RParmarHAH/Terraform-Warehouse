create or replace view DW_PROD.STAGE.VW_CURRENT_DELETED_MATRIXCARE_DIM_SERVICES(
	SERVICE_KEY
) as
SELECT 
MD5('MATRIXCARE'||'-' || SVCC_ID ||'-'|| 'MATRIXCARE') AS SERVICE_KEY
FROM DISC_PROD.MATRIXCARE.HIST_STVHC_T_SERVICECODE
WHERE ETL_DELETED_FLAG = TRUE AND ETL_LAST_UPDATED_DATE >= '1900-01-01'
AND CAST(ETL_LAST_UPDATED_DATE AS DATE) IN 
(SELECT MAX( ETL_LAST_UPDATED_DATE::DATE) FROM DISC_PROD.MATRIXCARE.HIST_STVHC_T_SERVICECODE);