create or replace view DW_PROD.REPORT.VW_HOURS_AND_CENSUS_BUDGETEDDATA_DAILY(
	BUDGET_DATE,
	OFFICE_STATE_CODE,
	OFFICE_NUMBER,
	BRANCH_KEY,
	SERVICELINE,
	HOURS_BUDGETED_DAILY
) as
SELECT CALENDAR_DATE AS BUDGET_DATE, STATE_CODE AS OFFICE_STATE_CODE, OFFICE_NUMBER, BRANCH_KEY, SERVICE_LINE_CODE AS SERVICELINE,
	SUM(HOURS_BUDGETED_DAILY) AS HOURS_BUDGETED_DAILY
	FROM INTEGRATION.FACT_BUDGET_MERGED 
    WHERE CALENDAR_DATE >= '2020-01-01'
    GROUP BY 1,2,3,4,5;