CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_ALAYACARE_FACT_CAREGIVER_TOUCH_POINT()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
  return_result VARCHAR;
BEGIN
DELETE FROM DW_PROD.HAH.FACT_CAREGIVER_TOUCH_POINT WHERE SOURCE_SYSTEM_ID = 9 AND INTERACTION_KEY  NOT IN (SELECT INTERACTION_KEY FROM DW_PROD.STAGE.ALAYACARE_FACT_CAREGIVER_TOUCH_POINT);
SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
return return_result;
END;
';