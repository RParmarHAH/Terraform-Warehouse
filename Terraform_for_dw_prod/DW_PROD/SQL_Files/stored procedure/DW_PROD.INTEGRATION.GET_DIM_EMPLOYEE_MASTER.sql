CREATE OR REPLACE PROCEDURE DW_PROD.INTEGRATION.GET_DIM_EMPLOYEE_MASTER("STR_ETL_TASK_KEY" VARCHAR(16777216), "STR_CDC_START" VARCHAR(16777216), "STR_CDC_END" VARCHAR(16777216))
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
	BEGIN
		TRUNCATE table DW_PROD.INTEGRATION.DIM_EMPLOYEE_MASTER;
		
		INSERT INTO DW_PROD.INTEGRATION.DIM_EMPLOYEE_MASTER select * from DW_PROD.HAH.DIM_EMPLOYEE;
		
		CALL DW_PROD.INTEGRATION.GET_EMP_MASTER_GA(-1,''1900-01-01'',''2099-12-31'');
		
		CALL DW_PROD.INTEGRATION.GET_DIM_EMPLOYEE_MASTER_CCSI_DFLEX_IL(-1,''1900-01-01'',''2099-12-31'');
		
		RETURN ''SUCCEED'';
		
END;
';