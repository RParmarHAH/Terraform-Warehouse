CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_ALLIANCE_DIM_INVOICE()
RETURNS VARCHAR(1677216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
BEGIN
DELETE FROM DW_PROD.HAH.DIM_INVOICE WHERE SOURCE_SYSTEM_ID=19
	AND SYSTEM_CODE=''ALLIANCE'' AND INVOICE_KEY
	NOT IN (SELECT INVOICE_KEY FROM DW_PROD.STAGE.ALLIANCE_DIM_INVOICE);
RETURN ''SUCCESS'';
END;
';