CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_ASR_DIM_CONTRACT()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
  return_result VARCHAR;
BEGIN
DELETE FROM DW_PROD.HAH.DIM_CONTRACT WHERE SOURCE_SYSTEM_ID = 13 AND CONTRACT_KEY  NOT IN (SELECT CONTRACT_KEY FROM DW_PROD.STAGE.ASR_DIM_CONTRACT WHERE SOURCE_SYSTEM_ID = 13);
SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
return return_result;
END;
';