CREATE OR REPLACE PROCEDURE DW_PROD.HAH.GET_STAGE_ALAYACARE_DIM_INVOICE_VISIT_LINKAGE("STR_ETL_TASK_KEY" VARCHAR(16777216), "STR_CDC_START" VARCHAR(16777216), "STR_CDC_END" VARCHAR(16777216))
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
BEGIN
--*****************************************************************************************************************************
-- NAME:  ALAYACARE_DIM_INVOICE_VISIT_LINKAGE
--
-- DEVELOPMENT LOG:A
-- DATE         AUTHOR              NOTES:
-- --------     ------------------- -----------------------------------------------------------------------------------------------
-- 06/12/23		Preeti Sharma		Initial Development

--*****************************************************************************************************************************
--
INSERT OVERWRITE INTO STAGE.ALAYACARE_DIM_INVOICE_VISIT_LINKAGE
WITH CLIENT AS 
(
SELECT * FROM 
(
	SELECT CLIENT_ID,MASTER_ID,CLIENT_ID AS ORIGINAL_RECORD_ID,GUID,BRANCH_ID
	FROM  DISC_DEDUPE_PROD.ALAYACARE.CLIENT_MASTER_LIST 
) 
UNION
SELECT * FROM 
(
	SELECT DISTINCT ID,MASTER_ID,ID AS ORIGINAL_RECORD_ID,GUID,BRANCH_ID
	FROM DISC_DEDUPE_PROD.ALAYACARE.CLIENT_MATCH_LIST
	WHERE ID NOT IN (SELECT CLIENT_ID FROM DISC_DEDUPE_PROD.ALAYACARE.CLIENT_MASTER_LIST ) 
) 
)
SELECT 
DISTINCT
MD5(NVL(TRIM(B.PROPERTIES_TBL_GT_ACCOUNT_DESCRIPTION), ''Unknown'') || '' ('' || COALESCE(GROUPS.BRANCH_ID, SERVICES.BRANCH_ID, FUNDERS.BRANCH_ID, -1) || '')'' || ''-'' || INVOICE_ITEM.INVOICE_ITEM_ID || ''-'' || ''ALAYACARE'') AS REVENUE_KEY
,MD5(NVL(TRIM(B.PROPERTIES_TBL_GT_ACCOUNT_DESCRIPTION), ''Unknown'') || '' ('' || NVL(VISITS.BRANCH_ID,-1) || '')'' || ''-'' || NVL (VISITS.VISIT_ID,''-1'')|| ''-'' || ''ALAYACARE'') AS VISIT_KEY
,CAST(NVL (VISITS.VISIT_ID,''-1'') AS VARCHAR) AS VISIT_ID
--,DATE (DATE_TRUNC (DAY, VISITS.START_AT)) AS SERVICE_DATE
,CASE WHEN INVOICE_ITEM.VISIT_ID IS  NULL
 THEN DATE (DATE_TRUNC (DAY, BILL.START_DATE)) 
 ELSE DATE (DATE_TRUNC (DAY, VISITS.START_AT)) END AS SERVICE_DATE
,UPPER(NVL(TRIM(B.PROPERTIES_TBL_GT_ACCOUNT_DESCRIPTION), ''Unknown'') || '' ('' || COALESCE(GROUPS.BRANCH_ID, SERVICES.BRANCH_ID, FUNDERS.BRANCH_ID, -1) || '')'') AS SYSTEM_CODE
,9 AS SOURCE_SYSTEM_ID
,:STR_ETL_TASK_KEY AS ETL_TASK_KEY
,:STR_ETL_TASK_KEY AS ETL_INSERTED_TASK_KEY                     
,convert_timezone(''UTC'', CURRENT_TIMESTAMP)::timestamp_ntz as ETL_INSERTED_DATE
,CURRENT_USER as ETL_INSERTED_BY
,convert_timezone(''UTC'', CURRENT_TIMESTAMP)::timestamp_ntz as ETL_UPDATED_DATE
,CURRENT_USER as ETL_LAST_UPDATED_BY
,0 as ETL_DELETED_FLAG
FROM DISC_PROD.ALAYACARE.INVOICE_ITEM  INVOICE_ITEM
LEFT JOIN DISC_PROD.ALAYACARE.VISIT VISITS  
ON VISITS.VISIT_ID =INVOICE_ITEM.VISIT_ID
INNER JOIN DISC_PROD.ALAYACARE.INVOICE_DETAILS AS INVOICE_DETAIL ON INVOICE_ITEM.INVOICE_ID = INVOICE_DETAIL.INVOICE_ID
LEFT JOIN DISC_PROD.ALAYACARE.SERVICE AS SERVICES 
ON SERVICES.SERVICE_ID = COALESCE(INVOICE_ITEM.SERVICE_ID,VISITS.SERVICE_ID)
LEFT JOIN DISC_PROD.ALAYACARE.FUNDER AS FUNDERS ON FUNDERS.FUNDER_ID = INVOICE_DETAIL.INVOICE_FUNDER_ID
LEFT JOIN  CLIENT C
ON COALESCE(INVOICE_ITEM.CLIENT_ID, VISITS.CLIENT_ID) = C.CLIENT_ID AND COALESCE(VISITS.BRANCH_ID, SERVICES.BRANCH_ID,FUNDERS.BRANCH_ID) = C.BRANCH_ID	
LEFT JOIN DISC_PROD.ALAYACARE.TBL_GUID_COST_CENTRE_TIER_4 COST_CENTRE_MAPPING
ON COST_CENTRE_MAPPING.GUID_TO = C.GUID
LEFT JOIN DISC_PROD.ALAYACARE.TBL_COST_CENTRES_TIER_4 AS COST_CENTRE
ON COST_CENTRE.ID = COST_CENTRE_MAPPING.COST_CENTRE_ID
LEFT JOIN DISC_PROD.ALAYACARE.GROUPS AS GROUPS
ON GROUPS.BRANCH_ID = COST_CENTRE.PROPERTIES_BRANCH_ID
AND GROUPS.PROFILE_COMPANY = COST_CENTRE.PROPERTIES_DESCRIPTION
LEFT JOIN DISC_PROD.ALAYACARE.BRANCH B 
ON B.BRANCH_ID = COALESCE(GROUPS.BRANCH_ID, SERVICES.BRANCH_ID, FUNDERS.BRANCH_ID)
-- ADDED BY PREETI SHARMA ON  6/11/23 TO GET SERVICE DATE FOR THE RECORDS WITH VISIT ID NULL
LEFT JOIN DISC_PROD.ALAYACARE.INVOICE_ITEM_BILLING_PERIOD AS INVOICE_ITEM_BILLING_PERIOD 
ON INVOICE_ITEM.INVOICE_ITEM_ID = INVOICE_ITEM_BILLING_PERIOD.INVOICE_ITEM_ID
LEFT JOIN DISC_PROD.ALAYACARE.BILLING_PERIOD AS BILL
ON INVOICE_ITEM_BILLING_PERIOD.BILLINGPERIOD_ID = BILL.BILLING_PERIOD_ID
; 

RETURN ''SUCCESS'';
END;
';