CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_ZENDESK_FACT_SLA()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
    RETURN_RESULT VARCHAR (1000);
BEGIN
      DELETE FROM DW_PROD.HAH.FACT_SLA WHERE SOURCE_SYSTEM_ID = 25 AND TICKET_SLA_KEY NOT IN (SELECT TICKET_SLA_KEY FROM DW_PROD.STAGE.ZENDESK_FACT_SLA);
  
  SELECT CONCAT (''MESSAGE : '',"number of rows deleted",'' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
	RETURN return_result;
  END;
';