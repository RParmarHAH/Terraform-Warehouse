RETURNS VARCHAR(16777216)
LANGUAGE JAVASCRIPT
EXECUTE AS OWNER
AS '
   sqlCmd = `DELETE FROM DW_PROD.HAH.DIM_INVOICE WHERE SOURCE_SYSTEM_ID = 8 AND INVOICE_KEY \\
    NOT IN (SELECT INVOICE_KEY FROM DW_PROD.STAGE.CCSI_DIM_INVOICE)`;
