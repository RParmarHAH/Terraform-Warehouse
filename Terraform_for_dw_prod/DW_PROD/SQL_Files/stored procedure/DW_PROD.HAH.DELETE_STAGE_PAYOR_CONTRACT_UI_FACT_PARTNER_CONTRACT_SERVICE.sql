CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_PAYOR_CONTRACT_UI_FACT_PARTNER_CONTRACT_SERVICE()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
COMMENT='user-defined procedure'
EXECUTE AS OWNER
AS '
DECLARE
    RETURN_RESULT VARCHAR;
BEGIN
DELETE FROM DW_PROD.HAH.FACT_PARTNER_CONTRACT_SERVICE WHERE SOURCE_SYSTEM_ID = 28 AND SYSTEM_CODE =''Payor_Contract UI''
AND PARTNER_CONTRACT_SERVICE_KEY NOT IN (SELECT PARTNER_CONTRACT_SERVICE_KEY FROM DW_PROD."STAGE".PAYOR_CONTRACT_UI_FACT_PARTNER_CONTRACT_SERVICE);
SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
return return_result;
END;
';