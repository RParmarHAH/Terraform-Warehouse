CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_ALAYACARE_DIM_SERVICES()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
	return_result varchar(1000);
BEGIN
  DELETE FROM DW_PROD.HAH.DIM_SERVICES WHERE SOURCE_SYSTEM_ID = 9 AND SERVICE_KEY NOT IN (SELECT SERVICE_KEY FROM DW_PROD.STAGE.ALAYACARE_DIM_SERVICES WHERE SOURCE_SYSTEM_ID = 9);
 
   SELECT CONCAT(''Message : '',"number of rows deleted",'' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
   return return_result;
 
END;
';