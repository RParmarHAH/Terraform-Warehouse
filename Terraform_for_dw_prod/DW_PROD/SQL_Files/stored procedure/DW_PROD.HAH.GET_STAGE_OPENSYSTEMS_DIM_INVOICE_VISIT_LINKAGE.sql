CREATE OR REPLACE PROCEDURE DW_PROD.HAH.GET_STAGE_OPENSYSTEMS_DIM_INVOICE_VISIT_LINKAGE("STR_ETL_TASK_KEY" VARCHAR(16777216), "STR_CDC_START" VARCHAR(16777216), "STR_CDC_END" VARCHAR(16777216))
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
BEGIN
--*****************************************************************************************************************************
-- NAME:  OPENSYSTEMS_DIM_INVOICE_VISIT_LINKAGE
--
-- DEVELOPMENT LOG:A
-- DATE         AUTHOR              NOTES:
-- --------     ------------------- -----------------------------------------------------------------------------------------------
-- 06/12/23		Preeti Sharma		Initial Development

--*****************************************************************************************************************************
--
INSERT OVERWRITE INTO STAGE.OPENSYSTEMS_DIM_INVOICE_VISIT_LINKAGE
SELECT 
MD5(''OPENSYSTEMS'' || ''-'' || INV_HEAD.INVOICEHEADERID || ''-'' || INVOICE.INVOICEDETAILID || ''-'' || ''HHAEXCHANGE'') AS REVENUE_KEY
,MD5(CONCAT(''OPENSYSTEMS'') || ''-'' || NVL(VISITS.VISITID,INVOICE.VISITID) || ''-'' || ''HHAEXCHANGE'') AS VISIT_KEY
,CAST(NVL(VISITS.VISITID,INVOICE.VISITID) AS VARCHAR) AS VISIT_ID
,DATE_TRUNC(DAY, VR.VISITDATE::DATE) AS SERVICE_DATE
,CONCAT(''OPENSYSTEMS - '', NVL(TRIM(OFFICE.STATE),''NULL'')) AS SYSTEM_CODE
,17 AS SOURCE_SYSTEM_ID
,:STR_ETL_TASK_KEY AS ETL_TASK_KEY
,:STR_ETL_TASK_KEY AS ETL_INSERTED_TASK_KEY                     
,convert_timezone(''UTC'', CURRENT_TIMESTAMP)::timestamp_ntz as ETL_INSERTED_DATE
,CURRENT_USER as ETL_INSERTED_BY
,convert_timezone(''UTC'', CURRENT_TIMESTAMP)::timestamp_ntz as ETL_UPDATED_DATE
,CURRENT_USER as ETL_LAST_UPDATED_BY
,0 as ETL_DELETED_FLAG
FROM DISC_PROD.HHAEXCHANGEOPENSYSTEMS.TBLINVOICEDETAILS_REPL INVOICE
INNER JOIN DISC_PROD.HHAEXCHANGEOPENSYSTEMS.TBLINVOICEHEADER_REPL INV_HEAD
	ON INVOICE.INVOICEHEADERID = INV_HEAD.INVOICEHEADERID
LEFT JOIN DISC_PROD.HHAEXCHANGEOPENSYSTEMS.VISITINFO_REPL VISITS
ON  VISITS.VISITID = INVOICE.VISITID
JOIN DISC_PROD.HHAEXCHANGEOPENSYSTEMS.TBLVISITS_REPL VR
ON VISITS.VISITID = VR.VISITID
LEFT JOIN DISC_PROD.HHAEXCHANGEOPENSYSTEMS.OFFICE_OFFICES_REPL OFFICE
ON INVOICE.OFFICEID = OFFICE.OFFICEID
; 

RETURN ''SUCCESS'';
END;
';