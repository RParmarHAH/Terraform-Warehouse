CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_ALLIANCE_FACT_CLIENT_SERVICE()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
  return_result VARCHAR;
BEGIN
	
DELETE FROM DW_PROD.HAH.FACT_CLIENT_SERVICE WHERE SOURCE_SYSTEM_ID =19 AND CLIENT_SERVICE_KEY 
 NOT IN (SELECT CLIENT_SERVICE_KEY FROM DW_PROD.STAGE.ALLIANCE_FACT_CLIENT_SERVICE);

SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
return return_result;
END;
';