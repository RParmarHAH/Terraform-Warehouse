--PARSE_JSON(REPLACE(MESSAGE,''None'',''NULL'')):messageType as MESSAGE_TYPE, -- LIBRARY, INLINE
PARSE_JSON(REPLACE(STATS,''None'',''NULL'')):failed as FAILED,
