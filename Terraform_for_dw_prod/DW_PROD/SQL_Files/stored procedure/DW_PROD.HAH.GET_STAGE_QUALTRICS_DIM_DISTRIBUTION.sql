''QUALTRICS'' AS SURVEY_SOURCE_TYPE,
--OWNER_ID, 
--ORGANIZATION_ID,
REQUEST_STATUS AS DISTRIBUTION_STATUS, -- Scheduled,Finished,Done,Generated
REQUEST_TYPE AS DISTRIBUTION_TYPE, -- Reminder,GeneratedInvite,Invite
