PARSE_JSON(REPLACE(STATS,''None'',''NULL'')):credits as CREDITS,
PARSE_JSON(REPLACE(STATS,''None'',''NULL'')):segments as SEGMENTS,
PARSE_JSON(REPLACE(STATS,''None'',''NULL'')):blocked as BLOCKED,
