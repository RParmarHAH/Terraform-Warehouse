CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_CCSI_DIM_SUPERVISOR()
RETURNS VARCHAR(16777216)
LANGUAGE JAVASCRIPT
EXECUTE AS OWNER
AS '
  sqlCmd = `DELETE FROM DW_PROD.HAH.DIM_SUPERVISOR WHERE SOURCE_SYSTEM_ID = 8 AND SUPERVISOR_KEY \\
  NOT IN (SELECT SUPERVISOR_KEY FROM DW_PROD.STAGE.CCSI_DIM_SUPERVISOR)`;
  sqlStmt = snowflake.createStatement( {sqlText: sqlCmd} );
  rs = sqlStmt.execute();
  return ''Done'';
';