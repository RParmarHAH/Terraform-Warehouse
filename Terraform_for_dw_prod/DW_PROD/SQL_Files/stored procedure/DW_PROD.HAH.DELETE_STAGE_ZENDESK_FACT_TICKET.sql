CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_ZENDESK_FACT_TICKET()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
    return_result varchar(1000);
BEGIN
	DELETE FROM DW_PROD.HAH.FACT_TICKET WHERE SOURCE_SYSTEM_ID = 25 AND TICKET_KEY NOT IN (SELECT TICKET_KEY FROM DW_PROD.STAGE.ZENDESK_FACT_TICKET);
  
SELECT CONCAT(''Message : '',"number of rows deleted",'' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
return return_result;
END;
';