CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_PREFERRED_DIM_SUPERVISOR()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
  return_result VARCHAR;
BEGIN
DELETE FROM DW_PROD.HAH.DIM_SUPERVISOR WHERE SOURCE_SYSTEM_ID =17 AND SYSTEM_CODE = ''PREFERRED'' AND SUPERVISOR_KEY NOT IN (SELECT SUPERVISOR_KEY FROM DW_PROD.STAGE.PREFERRED_DIM_SUPERVISOR);
SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
return return_result;
END;
';