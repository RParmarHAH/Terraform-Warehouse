CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_OSHAH_DIM_BILLING()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
    RETURN_RESULT VARCHAR;
BEGIN
DELETE FROM DW_PROD.HAH.DIM_BILLING WHERE SOURCE_SYSTEM_ID = 17  AND SYSTEM_CODE ILIKE ''%OSHAH%''
AND BILLING_KEY NOT IN (SELECT BILLING_KEY FROM DW_PROD."STAGE".OSHAH_DIM_BILLING);
SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
return return_result;
END;
';