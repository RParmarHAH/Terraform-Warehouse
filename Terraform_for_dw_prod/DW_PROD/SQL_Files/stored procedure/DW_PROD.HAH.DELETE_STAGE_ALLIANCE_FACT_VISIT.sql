CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_ALLIANCE_FACT_VISIT()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
	return_result VARCHAR;
BEGIN
	DELETE FROM HAH.FACT_VISIT WHERE SOURCE_SYSTEM_ID = 19 AND VISIT_KEY NOT IN (SELECT VISIT_KEY FROM STAGE.ALLIANCE_FACT_VISIT);
	SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
	return return_result;
end;
';