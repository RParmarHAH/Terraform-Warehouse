CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_ALLIANCE_FACT_REVENUE()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
  BEGIN
  DELETE FROM DW_PROD.HAH.FACT_REVENUE WHERE SOURCE_SYSTEM_ID = 19 AND SYSTEM_CODE = ''ALLIANCE'' AND REVENUE_KEY
  NOT IN (SELECT REVENUE_KEY FROM DW_PROD.STAGE.ALLIANCE_FACT_REVENUE);
  
RETURN ''SUCCESS'';
END;
';