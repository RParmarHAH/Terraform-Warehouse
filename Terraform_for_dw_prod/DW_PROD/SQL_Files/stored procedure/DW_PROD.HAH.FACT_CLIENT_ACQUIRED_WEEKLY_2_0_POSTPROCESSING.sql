--MERIDIUS 
SELECT DISTINCT 
MD5(CLIENT_MERGED.CLIENT_KEY || VISIT.PARTNER_CONTRACT_SERVICE_KEY || BRANCH_MERGED.BRANCH_KEY || DD.HAH_CURRENT_WEEK_FIRST_DAY) AS CLIENT_ACQUIRED_KEY,
CLIENT_MERGED.ORIGINAL_CLIENT_KEY,
CLIENT_MERGED.CLIENT_KEY,
CLIENT_MERGED.CLIENT_NUMBER,
VISIT.SOURCE_SYSTEM_ID,
VISIT.SYSTEM_CODE,
