CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_DATAFLEXSYNCDATA_DIM_BRANCH()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
	day_Of_Week int;
	return_result VARCHAR;
BEGIN
	SELECT dayofweek(current_timestamp()) into day_Of_Week;
	if (day_Of_Week = 0) then
		DELETE FROM DW_PROD.HAH.DIM_BRANCH WHERE SOURCE_SYSTEM_ID = 3 AND BRANCH_KEY NOT IN (SELECT BRANCH_KEY FROM DW_PROD.STAGE.VW_CURRENT_DATAFLEXSYNCDATA_DIM_BRANCH);
	else
		DELETE FROM DW_PROD.HAH.DIM_BRANCH WHERE SOURCE_SYSTEM_ID = 3 AND BRANCH_KEY IN (SELECT BRANCH_KEY FROM DW_PROD.STAGE.VW_CURRENT_DELETED_DATAFLEXSYNCDATA_DIM_BRANCH);
	end if;
	SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
	return return_result;
end;
';