CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_OSHAH_FACT_BRANCH_PAYROLL_PERIODS()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
BEGIN 
DELETE FROM DW_PROD.HAH.FACT_BRANCH_PAYROLL_PERIODS 
	WHERE BRANCH_KEY IN (SELECT BRANCH_KEY FROM DW_PROD."STAGE".OSHAH_DIM_BRANCH)
	AND BRANCH_PAYROLL_PERIODS_KEY NOT IN (select BRANCH_PAYROLL_PERIODS_KEY from DW_PROD."STAGE".OSHAH_FACT_BRANCH_PAYROLL_PERIODS);
RETURN ''SUCCESS'';
END;
';