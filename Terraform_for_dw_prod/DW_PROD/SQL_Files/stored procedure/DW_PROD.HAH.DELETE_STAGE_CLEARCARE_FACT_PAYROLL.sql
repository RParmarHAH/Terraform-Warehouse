CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_CLEARCARE_FACT_PAYROLL()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
  return_result VARCHAR;
BEGIN
DELETE FROM DW_DEV.HAH.FACT_PAYROLL WHERE SOURCE_SYSTEM_ID =16 AND SYSTEM_CODE = ''CLEARCARE'' AND PAYROLL_KEY
 NOT IN (SELECT PAYROLL_KEY FROM DW_DEV.STAGE.CLEARCARE_FACT_PAYROLL);
SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
return return_result;
END;
';