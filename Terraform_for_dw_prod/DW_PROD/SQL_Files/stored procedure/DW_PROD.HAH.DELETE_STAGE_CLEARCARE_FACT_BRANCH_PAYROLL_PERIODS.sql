CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_CLEARCARE_FACT_BRANCH_PAYROLL_PERIODS()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
  return_result VARCHAR;
BEGIN
DELETE FROM HAH.FACT_BRANCH_PAYROLL_PERIODS WHERE OFFICE_STATE_CODE = ''OH'' AND BRANCH_PAYROLL_PERIODS_KEY 
 NOT IN (SELECT BRANCH_PAYROLL_PERIODS_KEY FROM STAGE.CLEARCARE_FACT_BRANCH_PAYROLL_PERIODS) AND DETAILED_OFFICE_NAME
 IN (SELECT DISTINCT DETAILED_OFFICE_NAME FROM STAGE.CLEARCARE_DIM_BRANCH);
SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
return return_result;
END;
';