CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_NAVISION_DIM_BRANCH()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
  RETURN_RESULT VARCHAR;
BEGIN
		DELETE FROM HAH.DIM_BRANCH WHERE SOURCE_SYSTEM_ID = 21 AND BRANCH_KEY NOT IN 
			(SELECT BRANCH_KEY FROM STAGE.NAVISION_DIM_BRANCH);
	SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
	RETURN RETURN_RESULT;
END;
';