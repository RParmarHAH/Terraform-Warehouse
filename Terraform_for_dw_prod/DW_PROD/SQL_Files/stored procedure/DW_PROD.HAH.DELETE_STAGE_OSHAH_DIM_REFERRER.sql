CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_OSHAH_DIM_REFERRER()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
  return_result VARCHAR;
BEGIN
	DELETE FROM DW_PROD.HAH.DIM_REFERRER 
	WHERE REFERRER_KEY NOT IN (
	SELECT REFERRER_KEY 
	FROM DW_PROD."STAGE".OSHAH_DIM_REFERRER 
	)
	AND SOURCE_SYSTEM_ID =''17''
	AND SYSTEM_CODE =''OSHAH'' ;
	SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
	return return_result;
END;
';