CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_COSTALSYNCDATA_FACT_INVOICE_TRACKING()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
    day_Of_Week int;
	return_result VARCHAR(1000);
BEGIN
	SELECT dayofweek(current_timestamp()) into day_Of_Week;
	if (day_Of_Week = 0) then
      DELETE FROM DW_PROD.HAH.FACT_INVOICE_TRACKING WHERE SOURCE_SYSTEM_ID IN (1,2) AND INVOICE_TRACKING_KEY NOT IN (SELECT INVOICE_TRACKING_KEY FROM DW_PROD.STAGE.VW_CURRENT_COSTALSYNCDATA_FACT_INVOICE_TRACKING);
  else
     DELETE FROM DW_PROD.HAH.FACT_INVOICE_TRACKING WHERE SOURCE_SYSTEM_ID IN (1,2) AND INVOICE_TRACKING_KEY IN (SELECT INVOICE_TRACKING_KEY FROM DW_PROD.STAGE.VW_CURRENT_DELETED_COSTALSYNCDATA_FACT_INVOICE_TRACKING);
  end if;
	SELECT CONCAT(''Message : '',"number of rows deleted",'' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
    return return_result;
END;
';