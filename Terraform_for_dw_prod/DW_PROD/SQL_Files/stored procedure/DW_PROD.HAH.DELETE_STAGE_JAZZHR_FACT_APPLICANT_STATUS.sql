CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_JAZZHR_FACT_APPLICANT_STATUS()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
  RETURN_RESULT VARCHAR(1000);
BEGIN
      DELETE FROM DW_PROD.HAH.FACT_APPLICANT_STATUS WHERE SOURCE_SYSTEM_ID = 20 AND APPLICANT_STATUS_KEY NOT IN (SELECT APPLICANT_STATUS_KEY FROM DW_PROD.STAGE.JAZZHR_FACT_APPLICANT_STATUS WHERE SOURCE_SYSTEM_ID = 20);
  SELECT CONCAT (''MESSAGE : '',"number of rows deleted",'' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
	RETURN return_result;
  END;
';