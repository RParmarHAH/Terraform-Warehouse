CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_MATRIXCARE_DIM_CONTRACT()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
	day_Of_Week int;
	return_result VARCHAR;
BEGIN
	SELECT dayofweek(current_timestamp()) into day_Of_Week;
	if (day_Of_Week = 0) then
		DELETE FROM HAH.DIM_CONTRACT WHERE SOURCE_SYSTEM_ID = 7 AND CONTRACT_KEY NOT IN (SELECT CONTRACT_KEY FROM STAGE.VW_CURRENT_MATRIXCARE_DIM_CONTRACT);
	else
		DELETE FROM HAH.DIM_CONTRACT WHERE SOURCE_SYSTEM_ID = 7 AND CONTRACT_KEY IN (SELECT CONTRACT_KEY FROM STAGE.VW_CURRENT_DELETED_MATRIXCARE_DIM_CONTRACT);
	end if;
	SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
	return return_result;
end;
';