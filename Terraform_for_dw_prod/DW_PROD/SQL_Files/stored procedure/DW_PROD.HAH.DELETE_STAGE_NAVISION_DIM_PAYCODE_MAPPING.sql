CREATE OR REPLACE PROCEDURE DW_PROD.HAH.DELETE_STAGE_NAVISION_DIM_PAYCODE_MAPPING()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
  RETURN_RESULT VARCHAR;
BEGIN
		DELETE FROM HAH.DIM_PAYCODE_MAPPING WHERE SOURCE_SYSTEM_ID = 21 AND PAYCODE_MAPPING_KEY
    		NOT IN (SELECT PAYCODE_MAPPING_KEY FROM STAGE.NAVISION_DIM_PAYCODE_MAPPING);
	SELECT CONCAT(''Message : '',"number of rows deleted", '' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
	RETURN RETURN_RESULT;
END;
';