CREATE OR REPLACE TABLE DISC_PROD.DATA_CLEANSING.BRANCH_STANDARDIZED_ADDRESSES (
	SOURCE_SYSTEM_ID NUMBER(38,0) NOT NULL,
	SYSTEM_CODE VARCHAR(20) NOT NULL,
	OFFICE_NUMBER VARCHAR(50) NOT NULL,
	OFFICE_CODE VARCHAR(100) COMMENT 'First name of employee',
	OFFICE_NAME VARCHAR(100) COMMENT 'Last name of employee',
	ADDRESS VARCHAR(1000) COMMENT 'Address',
	CITY VARCHAR(500) COMMENT 'City',
	STATE VARCHAR(100) COMMENT 'State',
	ZIP VARCHAR(20) COMMENT 'Zip (5 digits)',
	POINTS VARCHAR(256) COMMENT '[lat,long] format of the coordinates',
	LATITUDE FLOAT COMMENT 'Latitude',
	LONGITUDE FLOAT COMMENT 'Longitude',
	LABEL VARCHAR(256) COMMENT 'Address label which does not include the secondary unit designator',
	ADDRESS_NUMBER VARCHAR(10) COMMENT 'The street address number',
	STREET VARCHAR(256) COMMENT 'Street address which includes the secondary unit designator',
	MUNICIPALITY VARCHAR(256) COMMENT 'Municipality - several counties or geographies can make up a municipality',
