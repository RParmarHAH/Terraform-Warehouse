CREATE OR REPLACE TABLE DISC_PROD.MATRIXCARE.MATRIXCARE_MASTER_BRANCH_MAPPING (
	BRANCH_KEY VARCHAR(32) NOT NULL,
	SOURCE_SYSTEM_ID NUMBER(38,0) NOT NULL,
	SYSTEM_CODE VARCHAR(20) NOT NULL,
	OFFICE_CODE VARCHAR(5) NOT NULL,
	OFFICE_NAME VARCHAR(100),
	CONVERGED_BRANCH_KEY VARCHAR(32) NOT NULL,
	CONVERGED_SOURCE_SYSTEM_ID NUMBER(38,0) NOT NULL,
	CONVERGED_SYSTEM_CODE VARCHAR(20) NOT NULL,
	CONVERGED_OFFICE_CODE VARCHAR(5) NOT NULL,
	CONVERGED_OFFICE_NAME VARCHAR(100),
	ETL_LAST_UPDATED_DATE TIMESTAMP_NTZ(9) NOT NULL DEFAULT CAST(CONVERT_TIMEZONE('UTC', CAST(CURRENT_TIMESTAMP() AS TIMESTAMP_TZ(9))) AS TIMESTAMP_NTZ(9)),
	constraint PK_MATRIXCARE_MASTER_BRANCH_MAPPING_BRANCH_KEY primary key (BRANCH_KEY),
	constraint UC_MATRIXCARE_MASTER_BRANCH_MAPPING_SYSTEMCODE_OFFICECODE unique (SYSTEM_CODE, OFFICE_CODE)
);