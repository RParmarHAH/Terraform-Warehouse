	PUBLISHED_TIME TIMESTAMP_NTZ(9) NOT NULL COMMENT 'Okta Event Timestamp',
	EVENT_TYPE VARCHAR(500),
	LOG_VERSION VARCHAR(50),
	SEVERITY VARCHAR(50),
	DISPLAY_MESSAGE VARCHAR(8000),
	ACTOR_NAME VARCHAR(500),
	OUTCOME_RESULT VARCHAR(50),
	OUTCOME_REASON VARCHAR(8000),
	TARGET_NAMES VARCHAR(8000),
	TRANSACTION_ID VARCHAR(200),
	TRANSACTION_TYPE VARCHAR(100),
	IP_CHAIN VARCHAR(4000),
	EXTERNAL_SESSION_ID VARCHAR(200),
	IMPORT_BATCH_ID VARCHAR(100),
	RAW_JSON VARCHAR(16777216),
	ETL_TASK_KEY NUMBER(38,0) NOT NULL,
