CREATE OR REPLACE TABLE DISC_PROD.STAGE.COASTAL_OFFICE_MAPPING_PAYROLL (
	DB VARCHAR(5) NOT NULL,
	DEPARTMENT VARCHAR(7) NOT NULL,
	SOURCE_SYSTEM_ID NUMBER(38,0) NOT NULL,
	SYSTEM_CODE VARCHAR(20) NOT NULL,
	OFFICE_CODE VARCHAR(5) NOT NULL,
	ETL_LAST_UPDATED_DATE TIMESTAMP_NTZ(9) NOT NULL DEFAULT CAST(CONVERT_TIMEZONE('UTC', CAST(CURRENT_TIMESTAMP() AS TIMESTAMP_TZ(9))) AS TIMESTAMP_NTZ(9)),
	constraint UC_COASTAL_OFFICE_MAPPING_PAYROLL_SYSTEM_CODE_OFFICE_CODE unique (SYSTEM_CODE, OFFICE_CODE),
	constraint PK_COASTAL_OFFICE_MAPPING_PAYROLL_DB_DEPARTMENT primary key (DB, DEPARTMENT)
);