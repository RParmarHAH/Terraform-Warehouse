CREATE OR REPLACE TABLE DISC_PROD.PAYOR_CONTRACT_UI.PAYOR (
	PAYOR_ID NUMBER(38,0) NOT NULL COMMENT 'Arbitrary key for the payer',
	PAYOR_NAME VARCHAR(100) NOT NULL,
	PAYOR_TYPE_CODE VARCHAR(20),
	NOTES VARCHAR(5000),
	PARENT_PAYOR_ID NUMBER(38,0) COMMENT 'Arbitrary key for the parent payer',
	PARENT_PAYOR_NAME VARCHAR(100),
	ETL_TASK_KEY NUMBER(38,0) NOT NULL,
	ETL_INSERTED_TASK_KEY NUMBER(38,0) NOT NULL,
	ETL_INSERTED_DATE TIMESTAMP_NTZ(9) NOT NULL,
	ETL_INSERTED_BY VARCHAR(100) NOT NULL,
	ETL_LAST_UPDATED_DATE TIMESTAMP_NTZ(9) NOT NULL,
	ETL_LAST_UPDATED_BY VARCHAR(100) NOT NULL,
	UPDOPERATION VARCHAR(6) NOT NULL,
	DELETE_FLAG BOOLEAN,
	PAYOR_STATE_CODE VARCHAR(2),
	constraint PK_PAYER_PAYER_ID primary key (PAYOR_ID)
);