create or replace view DISC_PROD.FILEPRO.VW_H_MONITOR(
	"CLIENT NAME",
	DATE,
	"CREATED BY",
	"CREATED AT",
	"CREATED ON",
	COMMENTS,
	"COMMENTS #2",
	"COMMENTS #3",
	FOLLOWUP,
	DESK,
	POSTED,
	"EMPLOYEE NAME",
	"EMPLOYEE #",
	"FKEYS/TXCODE"
) as 
SELECT T.$1 AS "CLIENT NAME" ,
SUBSTR(T.$2,1,4)||'-'||SUBSTR(T.$2,5,2) AS DATE,
T.$3 AS "CREATED BY",
T.$4 AS "CREATED AT",
T.$5 AS "CREATED ON",
T.$6 AS "COMMENTS",
T.$7 AS "COMMENTS #2",
T.$8 AS "COMMENTS #3",
T.$9 AS "FOLLOWUP",
T.$10 AS "DESK", 
T.$11 AS "POSTED",
T.$12 AS "EMPLOYEE NAME",
T.$13 AS "EMPLOYEE #",
T.$14 AS "FKEYS/TXCODE"
FROM 
@DISC_PROD.STAGE.AZSTAGEPROD/FilePro/HMonitor
(file_format => DISC_PROD.PUBLIC.CSV_FORMAT ) AS T;