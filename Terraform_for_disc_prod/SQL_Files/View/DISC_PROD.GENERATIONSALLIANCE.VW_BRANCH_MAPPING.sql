create or replace view DISC_PROD.GENERATIONSALLIANCE.VW_BRANCH_MAPPING(
	LOCATION_ID,
	NAME,
	ADDRESS1,
	ADDRESS2,
	PHONENUMBER,
	TOLLFREEPHONE,
	FAX,
	STATE,
	CITY,
	ZIP,
	STATE_CODE,
	PARENT_BRANCH_NAME,
	PARENT_FLAG,
	PARENT_BRANCH_CODE
) as
SELECT
	LT.STR_LOCATIONID,
	MP.NAME,
	MP.ADDRESS1,
	MP.ADDRESS2,
	MP.PHONENUMBER,
	MP.TOLLFREEPHONE,
	MP.FAX,
	MP.STATE,
	MP.CITY,
	MP.ZIP,
	MP.STATE_CODE,
	MP.PARENT_BRANCH_NAME,
	MP.PARENT_FLAG::BOOLEAN AS PARENT_FLAG,
	MP.PARENT_BRANCH_CODE
FROM
	DISC_PROD.GENERATIONSALLIANCE.TBL_LOCATION LT
LEFT JOIN DISC_PROD.GENERATIONSALLIANCE.LOCATION_MAPPING MP ON
	LT.STR_LOCATIONID = MP.LOCATION_ID
	WHERE LT.STR_LOCATIONID NOT IN ('ZBO');