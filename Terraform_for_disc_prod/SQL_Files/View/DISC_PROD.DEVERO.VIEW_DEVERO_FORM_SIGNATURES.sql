create or replace view DISC_PROD.DEVERO.VIEW_DEVERO_FORM_SIGNATURES(
	FORM_ID,
	SIGNATURE_DATE,
	CAREGIVER_CODE,
	ETL_TASK_KEY,
	ETL_INSERTED_TASK_KEY,
	ETL_INSERTED_DATE,
	ETL_INSERTED_BY,
	ETL_LAST_UPDATED_DATE,
	ETL_LAST_UPDATED_BY,
	ETL_DELETED_FLAG
) as (
WITH RESULT_DATA AS
(
SELECT DISTINCT
		  F.FORM_ID
		, XMLGET(FORM_SIGNATURES.VALUE, 'SIGNATURE_DATE'):"$"::STRING AS SIGNATURE_DATE
	    , XMLGET(FORM_SIGNATURES.VALUE, 'CAREGIVER_CODE'):"$"::STRING AS CAREGIVER_CODE
	 	, F.ETL_TASK_KEY
		, F.ETL_INSERTED_TASK_KEY
		, F.ETL_INSERTED_DATE
		, F.ETL_INSERTED_BY
		, F.ETL_LAST_UPDATED_DATE
		, F.ETL_LAST_UPDATED_BY
		, F.ETL_DELETED_FLAG
		, ROW_NUMBER() OVER(PARTITION BY F.FORM_ID ORDER BY DATE_MODIFIED DESC) AS LATEST_MODIFIED
FROM DISC_PROD.DEVERO.HIST_DEVERO_FORM F(NODE)
,LATERAL FLATTEN (XMLGET(PARSE_XML(F.FORM_XML) ,'FORM_SIGNATURES'):"$") FORM_SIGNATURES
--WHERE DATE(F.ETL_LAST_UPDATED_DATE) = COALESCE((
--SELECT MAX(DATE(ETL_LAST_UPDATED_DATE)) FROM DISC_PROD.DEVERO.HIST_DEVERO_FORM),DATE(GETDATE()))
)
SELECT 
FORM_ID
		, SIGNATURE_DATE
	    , CAREGIVER_CODE
	    , ETL_TASK_KEY
		, ETL_INSERTED_TASK_KEY
		, ETL_INSERTED_DATE
		, ETL_INSERTED_BY
		, ETL_LAST_UPDATED_DATE
		, ETL_LAST_UPDATED_BY
		, ETL_DELETED_FLAG
FROM RESULT_DATA
WHERE LATEST_MODIFIED = 1
);