create or replace view DISC_PROD.MATRIXCARE.VW_STVHC_T_AUTHORIZATION(
	AUTH_ID,
	AUTH_ADMISSIONID,
	AUTH_PAYERID,
	AUTH_SERVICECODEID,
	AUTH_UNITFLAG,
	AUTH_BEGINDATE,
	AUTH_ENDDATE,
	AUTH_CODE,
	AUTH_ACTIVEFLAG,
	AUTH_NOTES,
	AUTH_CREATEDDATE,
	AUTH_CREATEDUSER,
	AUTH_MODIFIEDDATE,
	AUTH_MODIFIEDUSER,
	AUTH_TS,
	AUTH_POCID,
	INSERTDATE,
	UPDATEDATE,
	DELETEDFLAG,
	SYS_CHANGE_VERSION,
	SYS_CHANGE_OPERATION,
	ETL_TASK_KEY,
	ETL_INSERTED_TASK_KEY,
	ETL_INSERTED_DATE,
	ETL_INSERTED_BY,
	ETL_LAST_UPDATED_DATE,
	ETL_LAST_UPDATED_BY,
	ETL_DELETED_FLAG,
	AUTH_BEGINDATE_EST,
	AUTH_ENDDATE_EST
) as
SELECT * -- Using * Considering the fact that there can be new fields in future
		, CONVERT_TIMEZONE('America/New_York', AUTH_BEGINDATE)::DATE AS AUTH_BEGINDATE_EST
		, CONVERT_TIMEZONE('America/New_York', AUTH_ENDDATE)::DATE AS AUTH_ENDDATE_EST
FROM DISC_PROD.MATRIXCARE.STVHC_T_AUTHORIZATION;