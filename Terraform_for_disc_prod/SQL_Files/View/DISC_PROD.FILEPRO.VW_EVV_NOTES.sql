create or replace view DISC_PROD.FILEPRO.VW_EVV_NOTES(
	DATE,
	TIME,
	CLIENT,
	"WORKER #",
	"WORKER NAME",
	"NOTE 1",
	"NOTE 2",
	"NOTE 3",
	"NOTE 4",
	"FOLLOW-UP",
	DESK,
	PRINTED,
	"PUNCH DATE",
	"PUNCH TIME",
	"PUNCH IN/OUT",
	"HAC HAS SMARTPHONE",
	"CALLER ID",
	"SHARED CODE"
) as 
SELECT SUBSTR(T.$1,1,4)||'-'||SUBSTR(T.$1,5,2)||'-'||SUBSTR(T.$1,7,2) AS DATE,
T.$2 AS TIME, 
T.$3 AS CLIENT,
T.$4 AS "WORKER #",
T.$5 AS "WORKER NAME",
T.$6 AS "NOTE 1",
T.$7 AS "NOTE 2",
T.$8 AS "NOTE 3",
T.$9 AS "NOTE 4",
T.$10 AS "FOLLOW-UP", 
T.$11 AS DESK,
T.$12 AS PRINTED,
T.$13 AS "PUNCH DATE",
T.$14 AS "PUNCH TIME",
T.$15 AS "PUNCH IN/OUT",
T.$16 AS "HAC HAS SMARTPHONE",
T.$17 AS "CALLER ID",
T.$18 AS "SHARED CODE"
FROM 
@DISC_PROD.STAGE.AZSTAGEPROD/FilePro/EVVnotes
(file_format => DISC_PROD.PUBLIC.CSV_FORMAT ) AS T;