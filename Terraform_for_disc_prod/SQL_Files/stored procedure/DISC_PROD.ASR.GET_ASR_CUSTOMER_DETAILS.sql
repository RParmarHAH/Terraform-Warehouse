CREATE OR REPLACE PROCEDURE DISC_PROD.ASR.GET_ASR_CUSTOMER_DETAILS("TASKKEY" VARCHAR(16777216))
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS ' 
--*****************************************************************************************************************************
-- NAME:  DISC_PROD.ASR.GET_ASR_CUSTOMER_DETAILS 
--
-- PURPOSE: To Load Discovery Layer Tables
--
-- DEVELOPMENT LOG:
-- DATE        		AUTHOR                	NOTES:
-- ----------  		-------------------   	-----------------------------------------------------------------------------------------------
-- 2023-11-20 		Ravi Suthar            	Initial Development
--*****************************************************************************************************************************

BEGIN 
    --TargetSQL
    COPY INTO DISC_PROD.ASR.HIST_ASR_CUSTOMER_DETAILS FROM (SELECT A.$1 AS CUSTOMERID, A.$2 AS CUSTOMER_EXTENDED_PROPERTY, A.$3 AS PROPERTY_VALUE, :TaskKey AS ETL_TASK_KEY, :TaskKey AS ETL_INSERTED_TASK_KEY, CURRENT_TIMESTAMP::TIMESTAMP_NTZ(9) AS ETL_INSERTED_DATE, CURRENT_USER() AS ETL_INSERTED_BY, CURRENT_TIMESTAMP::TIMESTAMP_NTZ(9) AS ETL_LAST_UPDATED_DATE, CURRENT_USER() AS ETL_LAST_UPDATED_BY, FALSE AS ETL_DELETED_FLAG FROM @DISC_PROD.Stage.AWSAZSTAGEPROD/ASR/Daily_Files/ASR_Data_Extract_CustomerDetails(file_format => DISC_PROD.STAGE.PSV_FORMAT_ENCLOSED_BY_NONE) A);

    --ViewSQL
    CREATE OR REPLACE TABLE DISC_PROD.ASR.ASR_CUSTOMER_DETAILS AS WITH curr_v AS (SELECT CUSTOMERID, CUSTOMER_EXTENDED_PROPERTY, MAX(ETL_LAST_UPDATED_DATE) AS MAX_LAST_UPDATED_DATE FROM DISC_PROD.ASR.HIST_ASR_CUSTOMER_DETAILS WHERE CUSTOMER_EXTENDED_PROPERTY IS NOT NULL GROUP BY CUSTOMERID, CUSTOMER_EXTENDED_PROPERTY) SELECT t.* FROM DISC_PROD.ASR.HIST_ASR_CUSTOMER_DETAILS t INNER JOIN curr_v v ON t.CUSTOMERID = v.CUSTOMERID AND t.CUSTOMER_EXTENDED_PROPERTY = v.CUSTOMER_EXTENDED_PROPERTY AND t.ETL_DELETED_FLAG = FALSE and t.ETL_LAST_UPDATED_DATE = v.MAX_LAST_UPDATED_DATE;

    return ''Success'';
END;
';