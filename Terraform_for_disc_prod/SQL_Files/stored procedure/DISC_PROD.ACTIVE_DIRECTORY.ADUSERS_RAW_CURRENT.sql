CREATE OR REPLACE PROCEDURE DISC_PROD.ACTIVE_DIRECTORY.ADUSERS_RAW_CURRENT("STR_ETL_TASK_KEY" VARCHAR(16777216), "STR_CDC_START" VARCHAR(16777216), "STR_CDC_END" VARCHAR(16777216))
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '

CREATE OR REPLACE TABLE DISC_PROD.ACTIVE_DIRECTORY.ADUSERS_RAW_CURRENT AS

WITH CURR_V AS (
	SELECT 
		OBJECTGUID,
		MAX(ETL_LAST_UPDATED_DATE) AS ETL_LAST_UPDATED_DATE
	FROM DISC_PROD.ACTIVE_DIRECTORY.HIST_ADUSERS_RAW
	GROUP BY 1
)
SELECT 
	P.* EXCLUDE ETL_DELETED_FLAG,
	CASE
		WHEN ETL_INSERTED_DATE::DATE <> CURRENT_DATE THEN TRUE
		ELSE P.ETL_DELETED_FLAG
	END AS ETL_DELETED_FLAG
FROM DISC_PROD.ACTIVE_DIRECTORY.HIST_ADUSERS_RAW P
INNER JOIN CURR_V
	ON /*CURR_V.EMPLOYEEID = P.EMPLOYEEID 
	AND CURR_V.EMAILADDRESS = P.EMAILADDRESS*/
	CURR_V.ETL_LAST_UPDATED_DATE = P.ETL_LAST_UPDATED_DATE
	AND CURR_V.OBJECTGUID = P.OBJECTGUID
	AND ETL_DELETED_FLAG = FALSE;
	
';