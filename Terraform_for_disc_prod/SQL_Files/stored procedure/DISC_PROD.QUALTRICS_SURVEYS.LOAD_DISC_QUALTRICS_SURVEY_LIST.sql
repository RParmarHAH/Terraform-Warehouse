CREATE OR REPLACE PROCEDURE DISC_PROD.QUALTRICS_SURVEYS.LOAD_DISC_QUALTRICS_SURVEY_LIST()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
BEGIN
	INSERT OVERWRITE INTO DISC_PROD.QUALTRICS_SURVEYS.QUALTRICS_SURVEY_LIST 
	SELECT CURRENT_TIMESTAMP(),A.$1 FROM @DW_PROD.Stage.AZSTAGEPROD/Qualtrics/Qualtrics_Survey_List/(file_format => DW_PROD.PUBLIC.MY_JSON_FORMAT,pattern =>''.*Qualtrics_Survey_List.*.'') A;
	RETURN ''Succeeded.'';
END
';