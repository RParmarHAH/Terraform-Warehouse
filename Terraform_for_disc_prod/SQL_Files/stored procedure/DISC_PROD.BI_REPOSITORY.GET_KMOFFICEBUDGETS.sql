CREATE OR REPLACE PROCEDURE DISC_PROD.BI_REPOSITORY.GET_KMOFFICEBUDGETS("TASKKEY" VARCHAR(16777216))
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS ' 
BEGIN 
    --TargetSQL
    INSERT OVERWRITE INTO DISC_PROD.BI_Repository.kmOfficeBudgets (SELECT t.$1 AS DbName, t.$2 AS OfficeNumber, t.$3 AS ServiceLine, t.$4 AS Metric, t.$5 AS Month, t.$6 AS Value, t.$7 AS Source, :TaskKey AS ETL_TASK_KEY, :TaskKey AS ETL_INSERTED_TASK_KEY, current_timestamp::TIMESTAMP_NTZ(9) AS ETL_INSERTED_DATE, current_user() AS ETL_INSERTED_BY, current_timestamp::TIMESTAMP_NTZ(9) AS ETL_LAST_UPDATED_DATE, current_user() AS ETL_LAST_UPDATED_BY, DECODE(t.$9,''D'', True, False) AS ETL_DELETED_FLAG FROM @DISC_PROD.STAGE.AWSAZSTAGEPROD/To_Be_Processed/BI_REPOSITORY/BI_Repository_dbo_kmOfficeBudgets.csv.gz (FILE_FORMAT=>DISC_PROD.STAGE.MY_CSV_FORMAT) t);

    return ''Success'';
END;
';