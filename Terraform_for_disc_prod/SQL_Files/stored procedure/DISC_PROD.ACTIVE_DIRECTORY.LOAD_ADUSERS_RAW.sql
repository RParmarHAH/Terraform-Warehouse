CREATE OR REPLACE PROCEDURE DISC_PROD.ACTIVE_DIRECTORY.LOAD_ADUSERS_RAW("STR_ETL_TASK_KEY" VARCHAR(16777216), "STR_CDC_START" VARCHAR(16777216), "STR_CDC_END" VARCHAR(16777216))
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
	BEGIN
        CALL DISC_PROD.ACTIVE_DIRECTORY.ADUSERS_RAW(-1,''1900-01-01'',''2099-12-31'');
        CALL DISC_PROD.ACTIVE_DIRECTORY.ADUSERS_RAW_CURRENT(-1,''1900-01-01'',''2099-12-31'');
		CALL DISC_PROD.ACTIVE_DIRECTORY.HIST_ADUSERS_RAW_REFRESH(-1,''1900-01-01'',''2099-12-31'');
	END;
';