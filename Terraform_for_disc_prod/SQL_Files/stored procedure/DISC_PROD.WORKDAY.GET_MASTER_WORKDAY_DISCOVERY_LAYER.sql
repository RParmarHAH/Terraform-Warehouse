CREATE OR REPLACE PROCEDURE DISC_PROD.WORKDAY.GET_MASTER_WORKDAY_DISCOVERY_LAYER("TASKKEY" VARCHAR(16777216))
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS ' BEGIN 
CALL DISC_PROD.WORKDAY.GET_AUDIT(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_QUALIFICATIONS_CERTIFICATION_ACHIEVEMENT(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_RELATED_PERSON(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_RELATED_PERSON_ADDRESS_DATA(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_RELATED_PERSON_EMAIL_DATA(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_RELATED_PERSON_NAME_DATA(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_RELATED_PERSON_PHONE_DATA(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_REMOTE_LOCATIONS_POST_PROCESSING();
CALL DISC_PROD.WORKDAY.GET_WORKER_ADDITIONAL_INFORMATION(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_ADDRESS_DATA(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_COMPENSATION(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_EMAIL_DATA(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_IDENTIFICATION_DATA(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_LEAVE_OF_ABSENCE(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_NAME_DATA(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_ORGANIZATION_DATA(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_PERSONAL(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_PHONE_DATA(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_POSITION_DATA(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_STATUS(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_SUMMARY(:TASKKEY);
CALL DISC_PROD.WORKDAY.GET_WORKER_SUPERVISOR(:TASKKEY);
return ''Success'';
END;';