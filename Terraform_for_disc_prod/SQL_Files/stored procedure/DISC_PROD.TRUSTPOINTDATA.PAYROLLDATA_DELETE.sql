CREATE OR REPLACE PROCEDURE DISC_PROD.TRUSTPOINTDATA.PAYROLLDATA_DELETE("STR_ETL_TASK_KEY" VARCHAR(16777216), "STR_CDC_START" VARCHAR(16777216), "STR_CDC_END" VARCHAR(16777216))
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
DECLARE
  RETURN_RESULT VARCHAR(1000);
BEGIN        
   delete from "DISC_PROD"."TRUSTPOINTDATA"."PAYROLL" where REGEXP_LIKE(EMPLOYEE_ID, ''[a-zA-Z].*'') =''TRUE'' AND COMPANY = ''EXCEL'';
SELECT CONCAT (''MESSAGE : '',"number of rows deleted",'' Rows Deleted.'') into :return_result FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()));
	RETURN return_result;
  END;
';